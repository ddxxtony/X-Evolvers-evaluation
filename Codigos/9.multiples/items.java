package multiples;


// this class was created with the aim of having a way to store two valors on each field of the linked list
public class items {
    private int num;
    private String str;

    public items(int num, String str) {
        this.num = num;
        this.str = str;
    }

    public items() {
    }

    public int getNum() {
        return num;
    }

    public String getStr() {
        return str;
    }
    
     
}
